fn find_pid(name: &str) -> Option<u32> {
    unsafe {
        let snap = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS, 0);
        if snap == winapi::um::handleapi::INVALID_HANDLE_VALUE { return None; }

        let mut entry: PROCESSENTRY32W = std::mem::zeroed();
        entry.dwSize = std::mem::size_of::<PROCESSENTRY32W>() as u32;

        if Process32FirstW(snap, &mut entry) == 0 {
            CloseHandle(snap);
            return None;
        }

        loop {
            // CORRECT NULL-TERMINATED UTF-16 STRING EXTRACTION
            let null_idx = entry.szExeFile.iter()
                .position(|&c| c == 0)
                .unwrap_or(entry.szExeFile.len());

            let proc_name = String::from_utf16_lossy(&entry.szExeFile[..null_idx]);

            if proc_name.eq_ignore_ascii_case(name) {
                CloseHandle(snap);
                return Some(entry.th32ProcessID);
            }

            if Process32NextW(snap, &mut entry) == 0 {
                break;
            }
        }
        CloseHandle(snap);
    }
    None
}

